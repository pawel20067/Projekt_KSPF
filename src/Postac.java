
public abstract class Postac {
	protected String imie;
	protected String nazwisko;
	protected String narodowosc;
	protected int wiek;
	
	public Postac(String imie, String naziwsko, String narodowosc, int wiek) {
		this.imie=imie;
		this.nazwisko=naziwsko;
		this.narodowosc=narodowosc;
		this.wiek=wiek;
	}
}
